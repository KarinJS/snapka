import path from 'node:path'
import axios from '@karinjs/axios'
import { getText } from '../httpUtil.js'

import { BrowserPlatform } from './types.js'

function archive (platform: BrowserPlatform, buildId: string): string {
  switch (platform) {
    case BrowserPlatform.LINUX_ARM:
    case BrowserPlatform.LINUX:
      return 'chrome-linux'
    case BrowserPlatform.MAC_ARM:
    case BrowserPlatform.MAC:
      return 'chrome-mac'
    case BrowserPlatform.WIN32:
    case BrowserPlatform.WIN64:
      // Windows archive name changed at r591479.
      return parseInt(buildId, 10) > 591479 ? 'chrome-win' : 'chrome-win32'
  }
}

function folder (platform: BrowserPlatform): string {
  switch (platform) {
    case BrowserPlatform.LINUX_ARM:
    case BrowserPlatform.LINUX:
      return 'Linux_x64'
    case BrowserPlatform.MAC_ARM:
      return 'Mac_Arm'
    case BrowserPlatform.MAC:
      return 'Mac'
    case BrowserPlatform.WIN32:
      return 'Win'
    case BrowserPlatform.WIN64:
      return 'Win_x64'
  }
}

export function resolveDownloadUrl (
  platform: BrowserPlatform,
  buildId: string,
  baseUrl = 'https://storage.googleapis.com/chromium-browser-snapshots'
): string {
  return `${baseUrl}/${resolveDownloadPath(platform, buildId).join('/')}`
}

export function resolveDownloadPath (
  platform: BrowserPlatform,
  buildId: string
): string[] {
  return [folder(platform), buildId, `${archive(platform, buildId)}.zip`]
}

export function relativeExecutablePath (
  platform: BrowserPlatform,
  _buildId: string
): string {
  switch (platform) {
    case BrowserPlatform.MAC:
    case BrowserPlatform.MAC_ARM:
      return path.join(
        'chrome-mac',
        'Chromium.app',
        'Contents',
        'MacOS',
        'Chromium'
      )
    case BrowserPlatform.LINUX_ARM:
    case BrowserPlatform.LINUX:
      return path.join('chrome-linux', 'chrome')
    case BrowserPlatform.WIN32:
    case BrowserPlatform.WIN64:
      return path.join('chrome-win', 'chrome.exe')
  }
}
export async function resolveBuildId (
  platform: BrowserPlatform
): Promise<string> {
  const baseUrl = `https://registry.npmmirror.com/-/binary/chromium-browser-snapshots/${folder(platform)}/`
  const response = await axios.get<{ name: string }[]>(baseUrl).catch(() => null)
  if (response) {
    const versions = response.data?.[response?.data?.length - 1]?.name?.replace('/', '')
    if (versions) return versions
  }

  return await getText(
    new URL(
      `https://storage.googleapis.com/chromium-browser-snapshots/${folder(platform)}/LAST_CHANGE`
    )
  ).catch(() => {
    /**
     * 做个兜底
     * @time 2025年11月26日12:03:13
     */
    switch (platform) {
      case BrowserPlatform.LINUX_ARM:
      case BrowserPlatform.LINUX:
        return '1550223'
      case BrowserPlatform.MAC_ARM:
        return '1550222'
      case BrowserPlatform.MAC:
        return '1550202'
      case BrowserPlatform.WIN32:
        return '1550212'
      case BrowserPlatform.WIN64:
        return '1550184'
    }
  })
}

export function compareVersions (a: string, b: string): number {
  return Number(a) - Number(b)
}
